package main.java.com.giahuy.designpattem.Structural_pattern_ex6.ProxyPattern.model;

public class Item {
    private String name;
    private double price;

    public Item(String name, double price) {
        this.name = name;
        this.price = price;
    }

    @Override
    public String toString() {
        return name + " - $" + price;
    }
}
